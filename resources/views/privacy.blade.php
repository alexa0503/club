                        <p>一、会员奖励</p>
                        <p>车友会会员奖励分为风迷币和评级积分两类，会员注册成功之后即均可累积。</p>
                        <p>（一）风迷币</p>
                        <p>1.定义：会员通过指定的消费或参加指定会员活动获得的可用于兑换消费的奖励。</p>
                        <p>2.来源：购买指定车型、再购指定车型、推荐购车、购买原厂备件、维修保养等指定消费及参加公司指定的会员活动。</p>
                        <p>3.用途：网上商城兑换礼品、精品配件及电子抵用卷（可用于在经销商处消费）。风迷币不得转让、兑现。</p>
                        <p>4.有效期：在获取当年及下一个自然年内有效。</p>
                        <p>（二）积分 </p>
