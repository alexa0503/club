                        <p>2.来源：购买东风风光汽车入会、再购车、推荐购车、购买原厂配件、维修保养及参加车友会各项会员活动（预约维保，满意度调查，发正面贴等）。 </p>
                        <p>3.用途：评定会员等级。</p>
                        <p>4.有效期：在获取当年及下一个自然年内有效。</p>
