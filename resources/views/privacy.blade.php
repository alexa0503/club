                        <p>2.礼品、精品配件兑换：会员可用风迷币在车友会官方网上商城兑换礼品及精品配件，风迷币具体使用规则详见网上商城发布。</p>
                        <p>3.电子抵用卷兑换：会员可用风迷币在车友会官方网上商城兑换电子抵用卷，抵用卷可在经销商处购车、购买原厂配件、维修保养等消费时直接抵扣现金，风迷币具体使用规则详见网上商城发布。</p>
